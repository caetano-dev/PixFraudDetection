"""
Feature extraction orchestrator for the PixFraudDetection polyglot pipeline.

This script consumes the pre-aggregated Parquet files generated by the DuckDB Data Engine (02_aggregate_graph.sql).
It instantly constructs the 
NetworkX DiGraphs and runs the Python-native ML algorithms without the overhead 
of pandas-based relational merges and sliding window iterations.

Execution model: Map (Parallel Extraction) -> Reduce (Sequential Rank Stability)
    - Each temporal window is processed independently in a worker process via
      concurrent.futures.ProcessPoolExecutor, bypassing the Python GIL for
      CPU-bound NetworkX computations.
    - The stateful RankStabilityTracker (which compares Day T against Day T-1)
      runs sequentially after all parallel workers have completed.
"""

